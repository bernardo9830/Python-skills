#L'HTTP è il protocollo utilizzato per la comunicazione tra client e server.

#Come abbiamo visto quando un client vuole interagire con il server invia una richieste http ed ottiene una response http

#Le comunicazioni HTTP avvengono in formato di testo non cifrato, il che significa che i dati trasmessi tra il client e il server non sono crittografati.



#HTTPS:
#E' sempre un protocollo per la comunicazione però supera l'http dal punto di vista della sicurezza.
#HTTPS è una versione sicura di HTTP che utilizza un livello aggiuntivo di sicurezza chiamato SSL/TLS (Secure Sockets Layer/Transport Layer Security).
#Le comunicazioni HTTPS sono crittografate, il che significa che i dati trasmessi tra il client e il server sono resi più sicuri e meno vulnerabili agli attacchi.
#Questo è importante quando si cercano di inviare dati sensibili come quelli relativi ai pagamenti


