#                                                       FRAMEWORK

#Un framework è una raccolta di librerie e classi che forniscono una serie di funzionalità comuni.
#Queste librerie e classi sono progettate per essere utilizzate insieme per creare un tipo specifico di applicazione, come un'applicazione web o un'app mobile.


#FLASK

#Flask è un framework leggero per lo sviluppo di applicazioni web in Python. È progettato per essere semplice, flessibile e facile da estendere, il che lo rende una scelta popolare per sviluppatori Python che desiderano creare applicazioni web senza il peso di un framework più complesso.

#Templates: Flask supporta l'utilizzo di template per generare dinamicamente pagine HTML. Jinja2 è il motore di template predefinito utilizzato con Flask.

# Flask, un framework web leggero per Python, utilizza Werkzeug come uno dei suoi componenti principali.

#Werkzeug è un toolkit WSGI (Web Server Gateway Interface) che implementa oggetti di richiesta (requests), oggetti di risposta (response) e funzioni di utilità.

#Jinja2:inja2 è un motore di template per il linguaggio di programmazione Python ed è comunemente utilizzato con il framework web Flask. Il suo scopo principale è consentire la creazione di pagine web dinamiche incorporando logica di programmazione all'interno dei template HTML.
# con la sintassi {{user}} stiamo indicando una variabile mentre i divisori %...% servono per implementare delle logiche di programmazione come l'if e il for

#render_template Questo modulo ci permette di ottenere una pagina html

#redirect: ci permette di reindirizzare l'utente da una pagina all'altra

#session: il modulo session in Flask consente di memorizzare dati di sessione utente tra le richieste.

