#                                                       Files

# Un file è sostanzialmente una raccolta di dati.

#Un file può contenere diversi tipi di dati: audio, immaggini, testo etc...
#Quindi una caratteristica dei file è il file format : ovvero il formato dei file

#Per aiutare il computer a capire che tipo di dati raccoglie quel file, abbiamo il file extension.
#Il file extension è un'estensione che c'è alla fine del nome del file con la quale si riesce ad individuare il file format
#Per esempio i file di immaggini hanno come estensione .jpj, i file di testo di solito .txt, i file di video .mp3


#METADATA
#I metadata sono i dati che riguardano i tipi di dati dei file: ad esempio dimensione, ultimo accesso etc..
#I metadata consistono di file attribute. Ogni attributo ha due possibili stati: set (toggled on) o cleared (toggled off).
#Gli attributi del file dicono al sistema operativo come e se il file ha bisogno di un trattamento speciale
#Ad esempio può capitare che un file ha come attributo solamente la modalità lettura allora questo sta dicendo
#al nostro sistema operativo che può solamente leggerlo ma no consentire modifiche fin a quando non viene dichiarato
# questo attributo

# I path di un file si distungono in assoluti e relativi

#Un path di un file che inizia con una root directory è chiamato assoluto
