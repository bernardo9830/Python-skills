# FILE TYPES

#REGULAR FILES
# I regular files sono quelli dove di solito mantieni i tuoi dati (immagini, video, etc..)
# Directories facilitano l'organizzazione di tanti file
# Symbolic links contengono il link ad altri file

# I regular file possono essere divisi in

# - textual file: sono file di testo

# - binary file: sequenze di byte senza vincoli di encoding

# Le directory si dividono in:
#- root directory: si può definire come cartella che non fa riferimento a nessun'altra cartella

#- parent directory: directory che fa riferimento alla root directory





