#                                                  ARITMETHIC OPERATORS

#Le operazioni matematiche che si possono effettuare in python sono le seguenti:
#1)somma
a = 10 + 5
#2)differenza
b = 10 - 5

#3) moltiplicazione
c = a * b

#4) divisione
d = a / b # output is a float

#5) divisione intera

d_int = a // b #output parte intera

# Elevamento potenza

p = a ** 2

#Resto

r = a % b # output 0 in quanto considera il resto di a modulo b

#L'ordine delle operazioni aritmetiche è lo stesso che si segue nella matematica

#Terminologia: L'operatore è ciò che permette di performare l'operazione, infatti '+' è un operatore. I valori su cui
#l'operatore performa l'operazione sono chiamati operandi

#PEP
#per evitare di scrivere operazioni molto lunghe possiamo effettuare il seguente break prima o dopo l'operatore

perimetro = (a
             + b
             + p
             + r)
print(perimetro)


